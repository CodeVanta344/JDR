// Utility exports
export const getWeaponById = (id: string): Weapon | undefined =>
  WEAPONS_COMPLETE.find(w => w.id === id);

export const getWeaponsByType = (type: Weapon['type']): Weapon[] =>
  WEAPONS_COMPLETE.filter(w => w.type === type);

export const getWeaponsByTier = (tier: Weapon['tier']): Weapon[] =>
  WEAPONS_COMPLETE.filter(w => w.tier === tier);

export const getWeaponsBySet = (setId: string): Weapon[] =>
  WEAPONS_COMPLETE.filter(w => w.setId === setId);

export const getCursedWeapons = (): Weapon[] =>
  WEAPONS_COMPLETE.filter(w => w.properties.includes('Maudit'));

export const getLegendaryWeapons = (): Weapon[] =>
  WEAPONS_COMPLETE.filter(w => w.properties.includes('Légendaire'));

export const getCraftableWeapons = (): Weapon[] =>
  WEAPONS_COMPLETE.filter(w => w.craftable);
